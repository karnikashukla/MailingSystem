package com.intranet.mailingsystem.models;


public class CorporationModel {
    private String corporationName;
    private String corporationDomain;

    public String getCorporationName() {
        return corporationName;
    }

    public void setCorporationName(String corporationName) {
        this.corporationName = corporationName;
    }

    public String getCorporationDomain() {
        return corporationDomain;
    }

    public void setCorporationDomain(String corporationDomain) {
        this.corporationDomain = corporationDomain;
    }
}
